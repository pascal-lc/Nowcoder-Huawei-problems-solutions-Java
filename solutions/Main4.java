public class Main4 {
    public static void main(String[] args) {
        // 1. 创建一个数组
        int[] arr = new int[10];

        // 2. 遍历数组
        for (int i = 0; i < arr.length; i++) {

            // 3. 给数组元素赋值
            arr[i] = i;

            // 4. 输出数组元素
            System.out.println(arr[i]);
        }
    }
}
